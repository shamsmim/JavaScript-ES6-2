var num = prompt("Enter your number :")

if(num>=80)
    console.log("A+");
else if(num>=70)
    console.log("A");
else if(num>=60)
    console.log("A-");
else if(num>=50)
    console.log("B");
else if(num>=40)
    console.log("C");
else if(num>=33)
    console.log("D");
else
    console.log("Fail");